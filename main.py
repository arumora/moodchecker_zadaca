mood = input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, it will be better!")
elif mood == "excited":
    print("Woho, it is great to hear that!")
elif mood == "relaxed":
    print("I see, today is the day to chill out.")
else:
    print("I don't recognize this mood.")